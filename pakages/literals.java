package pakages;
public class literals {
    public static void main(String[] args) {
        byte age=9;
        int age1=99;
        short age2=22;
        long age3=888888888888l;   //only for long data type "l or L" is needed at last
        System.out.println(age);
        System.out.println(age1);
        System.out.println(age2);
        System.out.println(age3);
        float number=23.45f;
        double number2=23.44444d;  //f or d required at last of float and double if none used then default is double
        System.out.println(number);
        System.out.println(number2);
        //constant value which can be assigned to a variable is called literal
        String str="my name is alex";
        System.out.println(str);


    }
    
}
